`timescale 1ns/1ps
//============================================================
// Module : alu
// Description : Parameterized Arithmetic Logic Unit
// Author : Mihir
//============================================================

module alu #(
    parameter WIDTH = 8
)(
    input  wire [WIDTH-1:0] a,
    input  wire [WIDTH-1:0] b,
    input  wire [2:0] opcode,

    output reg  [WIDTH-1:0] result,
    output reg              zero,
    output reg              carry,
    output reg              overflow
);

reg [WIDTH:0] temp;

always @(*) begin

    // Default Values
    result   = 0;
    carry    = 0;
    overflow = 0;

    case(opcode)

        //----------------------------------------------------
        // ADD
        //----------------------------------------------------

        3'b000: begin
            temp     = a + b;
            result   = temp[WIDTH-1:0];
            carry    = temp[WIDTH];
            overflow = (~(a[WIDTH-1]^b[WIDTH-1])) &
                       (result[WIDTH-1]^a[WIDTH-1]);
        end

        //----------------------------------------------------
        // SUB
        //----------------------------------------------------

        3'b001: begin
            temp     = a - b;
            result   = temp[WIDTH-1:0];
            carry    = temp[WIDTH];
            overflow = (a[WIDTH-1]^b[WIDTH-1]) &
                       (result[WIDTH-1]^a[WIDTH-1]);
        end

        //----------------------------------------------------
        // AND
        //----------------------------------------------------

        3'b010:
            result = a & b;

        //----------------------------------------------------
        // OR
        //----------------------------------------------------

        3'b011:
            result = a | b;

        //----------------------------------------------------
        // XOR
        //----------------------------------------------------

        3'b100:
            result = a ^ b;

        //----------------------------------------------------
        // NOT
        //----------------------------------------------------

        3'b101:
            result = ~a;

        //----------------------------------------------------
        // Shift Left
        //----------------------------------------------------

        3'b110:
            result = a << 1;

        //----------------------------------------------------
        // Shift Right
        //----------------------------------------------------

        3'b111:
            result = a >> 1;

        default:
            result = 0;

    endcase

    //--------------------------------------------------------
    // Zero Flag
    //--------------------------------------------------------

    if(result == 0)
        zero = 1'b1;
    else
        zero = 1'b0;

end

endmodule
