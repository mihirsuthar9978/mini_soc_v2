`timescale 1ns/1ps
//============================================================
// Testbench : counter_tb
// Description : Testbench for Parameterized Counter
//============================================================

module counter_tb;

parameter WIDTH = 8;

// Inputs
reg clk;
reg rst;
reg enable;

// Outputs
wire [WIDTH-1:0] count;
wire overflow;

//------------------------------------------------------------
// DUT Instantiation
//------------------------------------------------------------

counter #(
    .WIDTH(WIDTH)
) dut (
    .clk(clk),
    .rst(rst),
    .enable(enable),
    .count(count),
    .overflow(overflow)
);

//------------------------------------------------------------
// Clock Generation
//------------------------------------------------------------

always #5 clk = ~clk;

//------------------------------------------------------------
// Stimulus
//------------------------------------------------------------

initial begin

    // Initialize
    clk    = 0;
    rst    = 1;
    enable = 0;

    // Waveform
    $dumpfile("counter.vcd");
    $dumpvars(0, counter_tb);

    $display("-----------------------------------------");
    $display("Time\tReset\tEnable\tCount\tOverflow");
    $display("-----------------------------------------");

    $monitor("%0t\t%b\t%b\t%0d\t%b",
              $time,rst,enable,count,overflow);

    //--------------------------------------------------------
    // Apply Reset
    //--------------------------------------------------------

    #20;
    rst = 0;

    //--------------------------------------------------------
    // Enable Counter
    //--------------------------------------------------------

    enable = 1;

    // Count for some cycles

    #150;

    //--------------------------------------------------------
    // Disable Counter
    //--------------------------------------------------------

    enable = 0;

    #30;

    //--------------------------------------------------------
    // Enable Again
    //--------------------------------------------------------

    enable = 1;

    #50;

    //--------------------------------------------------------
    // Reset Again
    //--------------------------------------------------------

    rst = 1;

    #20;

    rst = 0;

    enable = 1;

    #50;

    //--------------------------------------------------------
    // Finish Simulation
    //--------------------------------------------------------

    $finish;

end

endmodule
