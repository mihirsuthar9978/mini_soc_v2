`timescale 1ns/1ps
//============================================================
// Testbench : alu_tb
// Description : Verification of Parameterized ALU
//============================================================

module alu_tb;

parameter WIDTH = 8;

// Inputs
reg [WIDTH-1:0] a;
reg [WIDTH-1:0] b;
reg [2:0] opcode;

// Outputs
wire [WIDTH-1:0] result;
wire zero;
wire carry;
wire overflow;

//------------------------------------------------------------
// DUT
//------------------------------------------------------------

alu #(
    .WIDTH(WIDTH)
) dut (

    .a(a),
    .b(b),
    .opcode(opcode),

    .result(result),
    .zero(zero),
    .carry(carry),
    .overflow(overflow)

);

//------------------------------------------------------------
// Waveform
//------------------------------------------------------------

initial begin

    $dumpfile("alu.vcd");
    $dumpvars(0, alu_tb);

    $display("---------------------------------------------------------------");
    $display("Time\tOpcode\tA\tB\tResult\tZ\tC\tV");
    $display("---------------------------------------------------------------");

    $monitor("%0t\t%b\t%0d\t%0d\t%0d\t%b\t%b\t%b",
              $time,
              opcode,
              a,
              b,
              result,
              zero,
              carry,
              overflow);

end

//------------------------------------------------------------
// Test Sequence
//------------------------------------------------------------

initial begin

    //---------------------------------------
    // ADD
    //---------------------------------------

    a = 10;
    b = 5;
    opcode = 3'b000;

    #20;

    //---------------------------------------
    // SUB
    //---------------------------------------

    a = 20;
    b = 8;
    opcode = 3'b001;

    #20;

    //---------------------------------------
    // AND
    //---------------------------------------

    a = 8'b11001100;
    b = 8'b10101010;
    opcode = 3'b010;

    #20;

    //---------------------------------------
    // OR
    //---------------------------------------

    opcode = 3'b011;

    #20;

    //---------------------------------------
    // XOR
    //---------------------------------------

    opcode = 3'b100;

    #20;

    //---------------------------------------
    // NOT
    //---------------------------------------

    a = 8'b11110000;
    opcode = 3'b101;

    #20;

    //---------------------------------------
    // SHIFT LEFT
    //---------------------------------------

    a = 8'b00001111;
    opcode = 3'b110;

    #20;

    //---------------------------------------
    // SHIFT RIGHT
    //---------------------------------------

    opcode = 3'b111;

    #20;

    //---------------------------------------
    // ZERO FLAG CHECK
    //---------------------------------------

    a = 0;
    b = 0;
    opcode = 3'b000;

    #20;

    //---------------------------------------
    // CARRY CHECK
    //---------------------------------------

    a = 8'hFF;
    b = 8'h01;
    opcode = 3'b000;

    #20;

    //---------------------------------------
    // OVERFLOW CHECK
    //---------------------------------------

    a = 8'd127;
    b = 8'd1;
    opcode = 3'b000;

    #20;

    $display("--------------------------------");
    $display("ALU TEST COMPLETED");
    $display("--------------------------------");

    $finish;

end

endmodule
